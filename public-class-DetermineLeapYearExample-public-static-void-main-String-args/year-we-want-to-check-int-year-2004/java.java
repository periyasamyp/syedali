public class List {
 
        public static void main(String[] args) {
               
                
                int l = 50;
               
                System.out.println("Printing Odd numbers between 1 and " + l);
               
                for(int j=1; j <= l; j++){
                       
                        
                        if( j % 2 != 0){
                                System.out.print(j + " ");
                        }
                }
        }
}
